// Odd & Even Number in Loops

for(let a = 1; a <= 15; a++){
    if (a % 2 === 0) {   // for EVEN nnumber
        console.log(`${a}`);
    }
}



for(let b = 1; b <= 15; b++){
    if (b % 2 != 0) {   // for ODD nnumber
        console.log(`${b}`);
    }
}   // to get the ODD number we should have to put "!=""
    // in "if" condition as above